## returns a list of upperlevel cache manipulating functions
makeCacheMatrix <- function(x = matrix()) {
    m <- NULL   # cache of inverse matrix 
    set <- function(y) {   # Setting new input
        x <<- y
        m <<- NULL
    }
    get <- function() x   # current input
    setmtx <- function(mtx) m <<- mtx  # Put inverted matrix to cache
    getmtx <- function() m             # Get inverted matrix from cache
    list(set = set, get = get,
        setmtx = setmtx,
        getmtx = getmtx)
}

## Returns a inverse matrix, if its cache is already calculated use it, solve() otherwise
cacheSolve <- function(x, ...) {
## Return a matrix that is the inverse of 'x'
    m <- x$getmtx()
    if(!is.null(m)) {
        message("getting cached data")
        return(m)
    } else {
        message("No cached data, calculating...")
    }
    data <- x$get()
    m <- solve(data, ...)
    x$setmtx(m)
    m
}
